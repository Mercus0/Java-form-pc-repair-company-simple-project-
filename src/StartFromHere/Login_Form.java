    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name=jTextField1.getText().toString();
        String password=jPassword.getText().toString();
        if(!name.equals("") && (!password.equals(""))){
            try{
                Statement stmt=DatabaseConnection.getInstance().getConnection().createStatement();
            ResultSet rs=stmt.executeQuery("select * from userlogin where username='"+name+"' and password='"+password+"'");
            int count=0;
            while(rs.next()){
                count++;
            }
                if(count==1){
                        Main main=new Main();
                        this.setVisible(false);
                        main.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(this,"Wrong password");
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(this, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Please Fill username and password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

